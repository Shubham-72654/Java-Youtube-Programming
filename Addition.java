// Addition of two number basic.

import java.lang.*;

class Addition {
    // entry point function
    public static void main(String arg[]){

        // variable initialization
        int addition = 0;
        int a = 10;
        int b = 11;

        addition = a + b;

        System.out.println("Addition of " + a + " and " + b + " is : " + addition);
    }
}
